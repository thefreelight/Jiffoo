/**
 * Model Usage Audit Script (Task 3.1.1)
 *
 * Parses all .prisma files in the schema/ directory to extract model names, then greps
 * apps/api/src for `prisma.<modelName>` references and type imports
 * to classify each model as "active" or "dormant".
 *
 * Usage:
 *   node scripts/audit-model-usage.cjs
 */

const { readFileSync, readdirSync, statSync, writeFileSync } = require('fs');
const { join, extname } = require('path');

// ============================================================================
// Configuration
// ============================================================================

const SCHEMA_DIR = join(__dirname, '..', 'prisma', 'schema');
const SRC_DIR = join(__dirname, '..', 'src');
const OUTPUT_PATH = join(__dirname, '..', '..', '..', '.kiro', 'specs', 'platform-evolution-2026h2', 'model-audit.md');
const SCAN_EXTENSIONS = ['.ts', '.tsx'];

// ============================================================================
// 1. Parse all .prisma files in schema/ directory for model names
// ============================================================================

function parseModels(schemaDir) {
  const models = [];
  const regex = /^model\s+(\w+)\s+\{/gm;
  for (const file of readdirSync(schemaDir)) {
    if (!file.endsWith('.prisma')) continue;
    const content = readFileSync(join(schemaDir, file), 'utf-8');
    let match;
    while ((match = regex.exec(content)) !== null) {
      models.push(match[1]);
    }
  }
  return models.sort();
}

// ============================================================================
// 2. Scan source files for references
// ============================================================================

function getAllTsFiles(dir) {
  const results = [];
  for (const entry of readdirSync(dir)) {
    const fullPath = join(dir, entry);
    const stat = statSync(fullPath);
    if (stat.isDirectory()) {
      results.push(...getAllTsFiles(fullPath));
    } else if (SCAN_EXTENSIONS.includes(extname(fullPath))) {
      results.push(fullPath);
    }
  }
  return results;
}

function scanReferences(models, srcDir) {
  const files = getAllTsFiles(srcDir);
  const refs = new Map();

  for (const model of models) {
    refs.set(model, { model, files: [], prismaRefs: 0, typeRefs: 0 });
  }

  for (const file of files) {
    const content = readFileSync(file, 'utf-8');
    const relPath = file.replace(srcDir + '/', '');

    for (const model of models) {
      const ref = refs.get(model);

      // Count prisma.modelName references (use split for reliable counting)
      // Prisma client property names are camelCase (first letter lowercase)
      const camelModel = model.charAt(0).toLowerCase() + model.slice(1);
      const prismaPattern = 'prisma.' + camelModel;
      const prismaCount = content.split(prismaPattern).length - 1;
      if (prismaCount > 0) {
        ref.prismaRefs += prismaCount;
        if (!ref.files.includes(relPath)) {
          ref.files.push(relPath);
        }
      }

      // Count type references (only in files that import from @prisma/client)
      if (content.includes('@prisma/client') || content.includes('PrismaClient')) {
        const typeRegex = new RegExp('\\b' + model + '\\b', 'g');
        const typeMatches = content.match(typeRegex);
        if (typeMatches && typeMatches.length > 0) {
          ref.typeRefs += typeMatches.length;
        }
      }
    }
  }

  return refs;
}

// ============================================================================
// 3. Output report
// ============================================================================

function classifyModel(ref) {
  if (ref.prismaRefs > 0) return 'active';
  if (ref.typeRefs > 1) return 'active';
  if (ref.typeRefs === 1 && ref.files.length > 0) return 'active';
  return 'dormant';
}

function generateReport(refs) {
  const lines = [];
  lines.push('# Model Usage Audit Report');
  lines.push('');
  lines.push('> Generated by `scripts/audit-model-usage.cjs` (Task 3.1.1)');
  lines.push('> Scans `prisma/schema/` models and `apps/api/src/` for Prisma client references.');
  lines.push('');

  const active = [];
  const dormant = [];
  for (const ref of refs.values()) {
    if (classifyModel(ref) === 'active') {
      active.push(ref);
    } else {
      dormant.push(ref);
    }
  }

  lines.push('## Summary');
  lines.push('');
  lines.push('| Category | Count |');
  lines.push('|----------|-------|');
  lines.push('| Active   | ' + active.length + ' |');
  lines.push('| Dormant  | ' + dormant.length + ' |');
  lines.push('| **Total**| **' + (active.length + dormant.length) + '** |');
  lines.push('');

  // Sort active by prismaRefs descending
  active.sort((a, b) => b.prismaRefs - a.prismaRefs);
  dormant.sort((a, b) => a.model.localeCompare(b.model));

  lines.push('## Active Models');
  lines.push('');
  lines.push('| # | Model | prisma.* refs | Type refs | Files |');
  lines.push('|---|-------|--------------|-----------|-------|');
  active.forEach((ref, i) => {
    lines.push('| ' + (i + 1) + ' | `' + ref.model + '` | ' + ref.prismaRefs + ' | ' + ref.typeRefs + ' | ' + ref.files.length + ' |');
  });
  lines.push('');

  lines.push('## Dormant Models');
  lines.push('');
  if (dormant.length === 0) {
    lines.push('No dormant models found.');
  } else {
    lines.push('| # | Model | prisma.* refs | Type refs | Files |');
    lines.push('|---|-------|--------------|-----------|-------|');
    dormant.forEach((ref, i) => {
      lines.push('| ' + (i + 1) + ' | `' + ref.model + '` | ' + ref.prismaRefs + ' | ' + ref.typeRefs + ' | ' + ref.files.length + ' |');
    });
  }
  lines.push('');

  lines.push('## Dormant Model Details');
  lines.push('');
  for (const ref of dormant) {
    lines.push('### `' + ref.model + '`');
    lines.push('- prisma.* references: ' + ref.prismaRefs);
    lines.push('- Type references: ' + ref.typeRefs);
    if (ref.files.length > 0) {
      lines.push('- Files: ' + ref.files.join(', '));
    } else {
      lines.push('- Files: none');
    }
    lines.push('');
  }

  return lines.join('\n');
}

// ============================================================================
// Main
// ============================================================================

function main() {
  console.log('Parsing schema directory...');
  const models = parseModels(SCHEMA_DIR);
  console.log('Found ' + models.length + ' models');

  console.log('\nScanning apps/api/src for references...');
  const refs = scanReferences(models, SRC_DIR);

  // Quick summary
  let activeCount = 0;
  let dormantCount = 0;
  for (const ref of refs.values()) {
    if (classifyModel(ref) === 'active') activeCount++;
    else dormantCount++;
  }
  console.log('Active: ' + activeCount + ', Dormant: ' + dormantCount);

  const report = generateReport(refs);
  writeFileSync(OUTPUT_PATH, report, 'utf-8');
  console.log('\nReport written to: ' + OUTPUT_PATH);
}

main();
