#!/usr/bin/env node
/**
 * Theme SDK API Surface Snapshot Generator (Task 6.1.1)
 *
 * Generates a .d.ts snapshot of the public API surface for:
 * - @jiffoo/theme-api-sdk
 * - shared/src/types/theme.ts (ThemePackage interface + all page props)
 *
 * The snapshot is checked into git. CI runs a diff to detect breaking changes.
 * If the surface changes without a corresponding changeset, CI fails.
 *
 * Usage:
 *   node scripts/generate-theme-surface.cjs           # Generate snapshot
 *   node scripts/generate-theme-surface.cjs --check    # Verify (CI mode)
 */

const { execSync } = require('child_process');
const { readFileSync, writeFileSync, existsSync } = require('fs');
const { join } = require('path');

const ROOT = join(__dirname, '..', '..', '..');
const SNAPSHOT_DIR = join(ROOT, 'packages', 'theme-api-sdk', 'snapshots');
const SNAPSHOT_FILE = join(SNAPSHOT_DIR, 'api-surface.d.ts');

// ============================================================
// 1. Generate theme-api-sdk surface
// ============================================================

function generateThemeApiSdkSurface() {
  // Use tsc to emit declaration files
  const tsconfig = join(ROOT, 'packages', 'theme-api-sdk', 'tsconfig.types.json');
  const outDir = join(ROOT, 'packages', 'theme-api-sdk', 'dist', 'types');

  try {
    execSync(`npx tsc -p ${tsconfig} --outDir ${outDir}`, {
      cwd: ROOT,
      stdio: 'pipe',
    });
  } catch (err) {
    console.error('Failed to compile theme-api-sdk types');
    console.error(err.stderr?.toString() || err.message);
    process.exit(1);
  }

  // Read the generated index.d.ts
  const indexDts = join(outDir, 'index.d.ts');
  if (!existsSync(indexDts)) {
    console.error('Generated index.d.ts not found at', indexDts);
    process.exit(1);
  }

  return readFileSync(indexDts, 'utf-8');
}

// ============================================================
// 2. Generate theme types surface from shared
// ============================================================

function generateThemeTypesSurface() {
  const themeTypesFile = join(ROOT, 'packages', 'shared', 'src', 'types', 'theme.ts');
  if (!existsSync(themeTypesFile)) {
    console.error('Theme types file not found at', themeTypesFile);
    process.exit(1);
  }

  const content = readFileSync(themeTypesFile, 'utf-8');

  // Extract all export interface/type/enum declarations
  // This is a simplified extraction — we capture the full file content
  // since it's all type definitions
  return `// Theme type definitions from packages/shared/src/types/theme.ts
// This file is the contract surface for all theme packages.
${content}`;
}

// ============================================================
// 3. Combine and write snapshot
// ============================================================

function generateSnapshot() {
  console.log('Generating theme API surface snapshot...');

  const apiSdkSurface = generateThemeApiSdkSurface();
  const themeTypesSurface = generateThemeTypesSurface();

  const header = `// ============================================================
// Theme SDK API Surface Snapshot
// Generated by: scripts/generate-theme-surface.cjs
// DO NOT EDIT MANUALLY — run 'node scripts/generate-theme-surface.cjs' to regenerate.
//
// This file locks the public API surface of:
// 1. @jiffoo/theme-api-sdk (HTTP client for themes)
// 2. shared/src/types/theme.ts (ThemePackage interface + page props)
//
// CI verifies this snapshot matches the actual code.
// Breaking changes require a changeset (major/minor).
// ============================================================

`;

  const combined = header + '// ════════════════════════════════════════════════════════════\n' +
    '// Part 1: @jiffoo/theme-api-sdk\n' +
    '// ════════════════════════════════════════════════════════════\n\n' +
    apiSdkSurface +
    '\n\n// ════════════════════════════════════════════════════════════\n' +
    '// Part 2: Theme Type Definitions (shared/src/types/theme.ts)\n' +
    '// ════════════════════════════════════════════════════════════\n\n' +
    themeTypesSurface;

  writeFileSync(SNAPSHOT_FILE, combined, 'utf-8');
  console.log('Snapshot written to:', SNAPSHOT_FILE);
  console.log('Size:', combined.length, 'bytes');
}

// ============================================================
// 4. Check mode (CI)
// ============================================================

function checkSnapshot() {
  console.log('Checking theme API surface against snapshot...');

  if (!existsSync(SNAPSHOT_FILE)) {
    console.error('Snapshot file not found. Run "node scripts/generate-theme-surface.cjs" first.');
    process.exit(1);
  }

  // Generate current surface to a temp string
  const apiSdkSurface = generateThemeApiSdkSurface();
  const themeTypesSurface = generateThemeTypesSurface();

  const header = `// ============================================================
// Theme SDK API Surface Snapshot
// Generated by: scripts/generate-theme-surface.cjs
// DO NOT EDIT MANUALLY — run 'node scripts/generate-theme-surface.cjs' to regenerate.
//
// This file locks the public API surface of:
// 1. @jiffoo/theme-api-sdk (HTTP client for themes)
// 2. shared/src/types/theme.ts (ThemePackage interface + page props)
//
// CI verifies this snapshot matches the actual code.
// Breaking changes require a changeset (major/minor).
// ============================================================

`;

  const current = header + '// ════════════════════════════════════════════════════════════\n' +
    '// Part 1: @jiffoo/theme-api-sdk\n' +
    '// ════════════════════════════════════════════════════════════\n\n' +
    apiSdkSurface +
    '\n\n// ════════════════════════════════════════════════════════════\n' +
    '// Part 2: Theme Type Definitions (shared/src/types/theme.ts)\n' +
    '// ════════════════════════════════════════════════════════════\n\n' +
    themeTypesSurface;

  const expected = readFileSync(SNAPSHOT_FILE, 'utf-8');

  if (current === expected) {
    console.log('✅ API surface matches snapshot. No changes detected.');
    process.exit(0);
  } else {
    console.error('❌ API surface has changed!');
    console.error('');
    console.error('The public API surface does not match the committed snapshot.');
    console.error('If this is an intentional change:');
    console.error('  1. Run: node scripts/generate-theme-surface.cjs');
    console.error('  2. Add a changeset describing the change (major/minor)');
    console.error('  3. Commit the updated snapshot');
    console.error('');

    // Show a simple diff summary
    const currentLines = current.split('\n');
    const expectedLines = expected.split('\n');
    let added = 0, removed = 0;
    const maxLen = Math.max(currentLines.length, expectedLines.length);
    for (let i = 0; i < maxLen; i++) {
      if (currentLines[i] !== expectedLines[i]) {
        if (i < expectedLines.length && currentLines[i] === undefined) removed++;
        else if (i < currentLines.length && expectedLines[i] === undefined) added++;
        else { added++; removed++; }
      }
    }
    console.error(`Diff: +${added} lines, -${removed} lines`);
    process.exit(1);
  }
}

// ============================================================
// Main
// ============================================================

const isCheckMode = process.argv.includes('--check');
if (isCheckMode) {
  checkSnapshot();
} else {
  generateSnapshot();
}
